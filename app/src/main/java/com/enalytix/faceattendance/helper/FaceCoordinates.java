package com.enalytix.faceattendance.helper;

public class FaceCoordinates {

    public int left;
    public int top;
    public int right;
    public int bottom;

    public FaceCoordinates() {
    }

    public FaceCoordinates(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
